-- ============================================================
-- TABLAS
-- ============================================================

create table public.clientes (
  id_cliente bigint generated by default as identity primary key,
  nombre varchar(255) not null,
  direccion varchar(255),
  telefono varchar(20),
  correo_electronico varchar(255)
);

create table public.proveedor (
  codprovvedor bigint generated by default as identity primary key,
  proveedor varchar(100) not null,
  telefonoo bigint not null,
  direccion varchar(255) not null
);

create table public.productos (
  id_producto bigint generated by default as identity primary key,
  nombre varchar(255) not null,
  descripcion varchar(255),
  precio numeric(10,2) not null,
  proveedor bigint not null references public.proveedor(codprovvedor)
);

create table public.stockproductos (
  id_producto bigint primary key references public.productos(id_producto) on delete cascade,
  cantidad_en_stock integer not null
);

create table public.facturas (
  id_factura bigint generated by default as identity primary key,
  fecha date,
  total numeric(10,2),
  id_cliente bigint references public.clientes(id_cliente),
  estatus integer not null default 1
);

create table public.detalles_de_productos (
  id_detalle bigint generated by default as identity primary key,
  id_factura bigint references public.facturas(id_factura) on delete cascade,
  id_producto bigint references public.productos(id_producto),
  cantidad integer,
  precio_unitario numeric(10,2)
);

create table public.pagos (
  id_pago bigint generated by default as identity primary key,
  id_factura bigint references public.facturas(id_factura) on delete cascade,
  fecha date,
  monto numeric(10,2)
);

create table public.usuarios (
  idusuario bigint generated by default as identity primary key,
  usuario varchar(30) not null unique,
  nombre varchar(60) not null,
  correo varchar(60) not null,
  clave varchar(100) not null,
  rol integer not null default 1
);

create table public.configuracion (
  id bigint generated by default as identity primary key,
  nit bigint not null,
  nombre varchar(20) not null,
  razon_social varchar(30) not null,
  telefono bigint not null,
  mail varchar(39) not null,
  direccion varchar(59) not null,
  igv numeric(10,2) not null
);

create table public.detalle_temp (
  correlativo bigint generated by default as identity primary key,
  token_user varchar(20) not null,
  idproducto bigint not null references public.productos(id_producto),
  cantidad integer not null,
  precio_venta numeric(10,2) not null
);

